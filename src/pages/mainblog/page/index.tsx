import type { GetStaticProps, NextPage } from "next";
import Head from "next/head";
import styles from "src/styles/Home.module.css";
import { BlogArchive } from "src/components/PageContainer/BlogArchive";
import { Layout } from "src/components/Layout";
import { client } from "src/lib/miscrocms/client";
import type { Article } from "src/types/article";
import { Loader } from "@mantine/core";
import { Pagination } from "src/components/Pagenation";

 export type BlogPaginationProps = {
  blog: Array<Article>;
  totalCount:number;
};

const Blog: NextPage<BlogPaginationProps> = ({
  blog,
  totalCount,
}) => {
  return (
    <div className={styles.container}>
      <Head>
        <title>Blog</title>
        <meta name="description" content="Generated by create next app" />
      </Head>
      <Layout>
        <BlogArchive blog={blog} />
        <Pagination totalCount={totalCount} />
        <Loader className="m-auto" />
      </Layout>
    </div>
  );
};

export default Blog;
export const getStaticProps: GetStaticProps = async () => {
  const data = await client.get({
    endpoint: "mainblog",
    queries: { limit: 5, offset: 0 },
  });

  return {
    props: {
      blog: data.contents,
      totalCount: data.totalCount,
    },
  };
};
